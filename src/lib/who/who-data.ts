/**
 * Returns the correct WHO series for a given metric and sex.
 * For sex 'other' or null/undefined, defaults to female data.
 */
export function getWhoSeries(metric: Metric, sex: string | null | undefined): WhoSeries {
  const normalizedSex = sex === 'male' ? 'male' : 'female'
  switch (metric) {
    case 'weight': return normalizedSex === 'male' ? WHO_WEIGHT_MALE : WHO_WEIGHT_FEMALE
    case 'height': return normalizedSex === 'male' ? WHO_HEIGHT_MALE : WHO_HEIGHT_FEMALE
    case 'head':   return normalizedSex === 'male' ? WHO_HEAD_MALE   : WHO_HEAD_FEMALE
    case 'bmi':    return normalizedSex === 'male' ? WHO_BMI_MALE    : WHO_BMI_FEMALE
  }
}
