export const ALL_GROWTH_SLEEP_SOCIAL_CITATIONS: Citation[] = [
  ...SLEEP_GROWTH_CITATIONS,
  ...SOCIAL_GROWTH_CITATIONS,
]

/**
 * Lookup citation by ID across both domains
 */
export function getGrowthSleepSocialCitation(id: string): Citation | undefined {
  return ALL_GROWTH_SLEEP_SOCIAL_CITATIONS.find(c => c.id === id)
}

/**
 * Get all citations for a specific growth track domain
 */
export function getCitationsForGrowthDomain(domain: 'sleep' | 'social'): Citation[] {
  const map = {
    sleep: SLEEP_GROWTH_CITATIONS,
    social: SOCIAL_GROWTH_CITATIONS,
  }
  return map[domain]
}

/**
 * Get all verified citations across both domains
 */
export function getVerifiedGrowthCitations(): Citation[] {
  return ALL_GROWTH_SLEEP_SOCIAL_CITATIONS.filter(c => c.verified)
}

/**
 * Audit summary stats for growth track citations (sleep + social)
 */
export function getGrowthSleepSocialCitationAudit() {
  const total = ALL_GROWTH_SLEEP_SOCIAL_CITATIONS.length
  const verified = ALL_GROWTH_SLEEP_SOCIAL_CITATIONS.filter(c => c.verified).length
  const withDoi = ALL_GROWTH_SLEEP_SOCIAL_CITATIONS.filter(c => c.doi).length
  const withPmid = ALL_GROWTH_SLEEP_SOCIAL_CITATIONS.filter(c => c.pmid).length

  const gradeDistribution = ALL_GROWTH_SLEEP_SOCIAL_CITATIONS.reduce((acc, c) => {
    acc[c.grade] = (acc[c.grade] || 0) + 1
    return acc
  }, {} as Record<string, number>)

  const sleepTotal = SLEEP_GROWTH_CITATIONS.length
  const sleepVerified = SLEEP_GROWTH_CITATIONS.filter(c => c.verified).length
  const socialTotal = SOCIAL_GROWTH_CITATIONS.length
  const socialVerified = SOCIAL_GROWTH_CITATIONS.filter(c => c.verified).length

  return {
    total,
    verified,
    unverified: total - verified,
    withDoi,
    withPmid,
    gradeDistribution,
    verificationRate: `${Math.round((verified / total) * 100)}%`,
    byDomain: {
      sleep: { total: sleepTotal, verified: sleepVerified, rate: `${Math.round((sleepVerified / sleepTotal) * 100)}%` },
      social: { total: socialTotal, verified: socialVerified, rate: `${Math.round((socialVerified / socialTotal) * 100)}%` },
    },
  }
}
